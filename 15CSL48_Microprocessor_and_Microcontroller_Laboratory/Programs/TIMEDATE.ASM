.MODEL SMALL
.STACK
.DATA
	MSG DB 10,13,"CURRENT TIME AND DATE IS = $"
.CODE
	MOV AX,@DATA
	MOV DS,AX

	LEA DX,MSG
	MOV AH,9
	INT 21H

	MOV AH,2CH			; CAPTURE DATE
	INT 21H

	MOV AL,CH 			; CH = HOURS, CL = MINUTES
	CALL DISP

	MOV DL,':'
	MOV AH,2
	INT 21H 			; DISPLAY CONTENTS OF DL

	MOV AL,CL 			; DISPLAY MINUTES
	CALL DISP

	MOV DL,' '
	MOV AH,2
	INT 21H

	; DAY
	MOV AH,2AH 			; CAPTURE DAY
	INT 21H					; DH = MONTH, DL, DAY

	MOV AL,DL
	CALL DISP

	MOV DL,'/'
	MOV AH,2
	INT 21H

	; MONTH
	MOV AH,2AH
	INT 21H

	MOV AL,DH
	CALL DISP

	MOV DL,'/'
	MOV AH,2
	INT 21H

	; YEAR
	MOV AH,2AH
	INT 21H

	ADD CX,0C30H

	MOV AL,CH
	CALL DISP

	MOV AL,CL
	CALL DISP

	MOV AH,4CH
	INT 21H

DISP PROC NEAR
	AAM
	ADD AX,3030H
	MOV BX,AX

	MOV DL,BH
	MOV AH,2
	INT 21H

	MOV DL,BL
	MOV AH,2
	INT 21H
	RET
DISP ENDP
END