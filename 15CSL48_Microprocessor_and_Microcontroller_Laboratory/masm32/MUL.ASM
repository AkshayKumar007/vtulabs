INITDS MACRO
	MOV AX,@DATA
	MOV DS,AX
ENDM

INIT8255 MACRO
	MOV AL,CW
	MOV DX,CR
	OUT DX,AL
ENDM

INPB MACRO 				; PORT B IS INPUT PORT
	MOV DX,PB
	IN DX,AL
ENDM

OUTPA MACRO
	MOV DX,PA
	OUT DX,AL
ENDM

DISPLAY MACRO MSG
	LEA DX,MSG
	MOV AH,9
	INT 21H
ENDM

EXIT1 MACRO
	MOV AH,4CH
	INT 21H
ENDM

.MODEL SMALL
.STACK
.DATA
	M1 DB 10,13,'SET VALUE FOR X, THEN PRESS ANY KEY $'
	M2 DB 10,13,'SET VALUE FOR Y, THEN PRESS ANY KEY $'

	PA EQU 1C60H
	PB EQU 1C61H

	CR EQU 1C63
	CW DB 82H

.CODE
	INITDS
	INIT8255

	DISPLAY M1

	MOV AH,8  		; READS INPUT WITHOUT ANY DISPLAY
	INT 21H

	INPB 			; READS X, VALUE STORED IN AL

	MOV BL,AL 		; X MOVED TO BL

	DISPLAY M2 		; SAME THING FOR Y
	MOV AH,8
	INT 21H
	INPB

	MUL BL      	; AL * BL

	OUTPA 			; AX HAS TOTAL RESULT SO WE SHOW THE TWO BYTES WITH THIS CODE
	CALL DELAY
	MOV AL,AH
	OUTPA

	EXIT1

DELAY PROC
	MOV BX,4FFFH

B2: MOV CX,FFFFH
B1: LOOP B1
	DEC BX
	JNZ B2

	RET 			; THIS IS IMPORTANT, DON'T FORGET

DELAY ENDP

END